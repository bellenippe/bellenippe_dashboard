import mongoose from "mongoose";

const UserAdminSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const UserAdmin =
  mongoose.models.UserAdmin || mongoose.model("UserAdmin", UserAdminSchema);

export default UserAdmin;
